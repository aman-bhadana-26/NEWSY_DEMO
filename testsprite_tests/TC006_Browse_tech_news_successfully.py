        await page.goto("http://localhost:3000")
        try:
            await page.wait_for_load_state("domcontentloaded", timeout=5000)
        except Exception:
            pass
        
        # -> Open http://localhost:5000/api/news in a new tab to verify whether the news article list and pagination details are returned by the backend API.
