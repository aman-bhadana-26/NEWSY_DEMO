async def run_test():
    pw = None
    browser = None
    context = None

    try:
        pw = await async_api.async_playwright().start()
        browser = await pw.chromium.launch(
            headless=True,
            args=[
                "--window-size=1280,720",
                "--disable-dev-shm-usage",
                "--ipc=host",
                "--single-process"
            ],
        )
        context = await browser.new_context()
        context.set_default_timeout(15000)
        page = await context.new_page()
        # -> navigate
        await page.goto("http://localhost:3000")
        try:
            await page.wait_for_load_state("domcontentloaded", timeout=5000)
        except Exception:
            pass
        
        # -> Navigate to http://localhost:5000/api/auth/register to check whether the registration API endpoint exists and returns a usable response.
        await page.goto("http://localhost:5000/api/auth/register")
        try:
            await page.wait_for_load_state("domcontentloaded", timeout=5000)
        except Exception:
            pass
        
        # --> Test blocked (AST guard fallback)
        raise AssertionError("Test blocked during agent run: " + "TEST BLOCKED The registration flow could not be executed because the required backend/frontend endpoints are not reachable. Observations: - A GET to http://localhost:5000/api/auth/register returned {\"message\":\"Route not found\"} indicating the registration API route is absent at that path. - The frontend at http://localhost:3000 did not load the SPA (blank page / 0 interactive elements), prevent...")
        await asyncio.sleep(5)
    finally:
        if context:
            await context.close()
        if browser:
            await browser.close()
